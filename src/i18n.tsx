function detectLocale(): Locale {
  const languages = typeof navigator === 'undefined'
    ? []
    : [navigator.language, ...navigator.languages].filter(Boolean)
  return languages.some(lang => lang.toLowerCase().startsWith('zh')) ? 'zh' : 'en'
}

const I18nContext = createContext<{ locale: Locale; t: Translate; tText: (text: string) => string }>({
  locale: 'en' as Locale,
  t: (key: TranslationKey) => en[key],
  tText: (text: string) => text,
})

export function translateText(text: string): string {
  return detectLocale() === 'zh' ? (zhText[text] ?? text) : text
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const locale = detectLocale()
  const dict = dictionaries[locale]
  const t = (key: TranslationKey) => dict[key] ?? en[key]
  const tText = (text: string) => locale === 'zh' ? (zhText[text] ?? text) : text
  return (
    <I18nContext.Provider value={{ locale, t, tText }}>
      {children}
    </I18nContext.Provider>
  )
}

export function useI18n() {
  return useContext(I18nContext)
}
